                        <li>Sal, pimienta y perejil</li>
                    </ul>
                    <hr>
                    <b>PREPARACIÓN</b> 
                    <p>
                    Empecemos quitando la carne de la jaiba, con mucho cuidado para que no se nos salga nada de la corteza del crustáceo. 
                    La separamos y la reservamos. 
                    Una de las jaibas la cortamos en trozos y la salteamos a fuego alto, le ponemos dos dientes de ajo y un tomate, todo 
                    lo salteamos bien, le ponemos agua y lo dejamos reducir. Después lo trituramos y de ahí obtendremos nuestra deliciosa 
                    salsa americana de pescado que acompañará de un exquisito sabor a mar a nuestro plato.
                    <br><br>
                    Las verduras como la zanahoria, el pimentón y el zucchini las cortamos en largo y las salteamos con aceite de oliva con 
                    un poco de sal y pimienta a fuego fuerte.
                    El melón lo pelamos y lo cortamos muy fino para hacer la flor que se aprecia en la imagen.
                    Las habas las pelamos, las escaldamos, las ponemos un poco en agua hirviendo con sal, luego la pasamos a agua fría para 
                    que nos queden bien verdes. Luego la salteamos y una vez salteadas, fuera del fuego, las juntamos con la carne de la jaiba 
                    y ya tenemos todo listo para montar el plato tal como se aprecia en la fotografía. 
                    <br><br>
                    Se añade sal, pimienta y un poco de perejil y ¡bueno provecho! Bien sea en estas navidades o en cualquier ocasión especial 
