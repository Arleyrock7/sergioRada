                <h3><span class="ui-accordion-header-icon ui-icon ui-accordion-icon ui-accordion-icon-active"></span>POLLO EN SALSA DE CIRUELAS </h3>
                <div>
                  <p>
                    <b>INGREDIENTES</b>
                    <ul>
                        <li>Una pechuga de pollo</li>
                        <li>Ciruelas</li>
                        <li>Calabaza</li>
                        <li>Brotes de soja</li>
                        <li>Espinacas</li>
                        <li>Hojas de apio</li>
                        <li>Cebolla y ajo</li>
                        <li>Laurel y tomillo</li>
                        <li>Aceite de oliva, sal y pimienta</li>
                    </ul>
                    <hr>
                    <b>PREPARACIÓN</b> 
                    <p>
                    Primeramente deshuesamos la pechuga de pollo. Cogemos la pechuga y la ponemos a marinar con laurel, tomillo, pimienta y sal. 
                    Hacemos un rollo con ella y procedemos a congelarla. Para hacer el rollo podemos usar papel de plástico o papel film. Una vez 
                    que la tengamos congelada y con la forma de rollo que queremos, la sacamos y la ponemos a asar en una sartén, primero a fuego 
                    fuerte y luego más despacio por todos los lados por unos 40 minutos. 
                    Para la calabaza, procedemos a pelarla, la dejamos sin piel y la ponemos con sal, pimienta y aceite a 180 grados durante 15 minutos 
                    en el horno hasta que resulte asado y blanda por dentro. 
                    <br><br>
                    Para las ciruelas, cogemos las ciruelas frescas, las ponemos a asar, muy despacito cubiertas en un recipiente, durante una hora a 150 
                    grados centígrados. Una vez que estén las sacamos, las picamos, salteamos ajo, cebolla, aceite y pimienta junto a las ciruelas, después 
                    lo trituramos todo y obtendremos de este modo la salsa. 
                    <br><br>
                    Para la ensalada cogemos la espinaca con las hojas de apio, la lavamos muy bien, la cortamos en juliana. Los brotes de soja se 
                    ponen a 120 grados centígrados en el horno durante unos 10 minutos, dándole vuelta para que quede un poco crujiente. 
                    Con todo eso no nos queda más que montar el plato con arreglo a la imagen y servir. 

