                <h3><span class="ui-accordion-header-icon ui-icon ui-accordion-icon ui-accordion-icon-active"></span>MORRILLO DE RES GOURMET </h3>
                <div>
                  <p>
                    <b>INGREDIENTES</b>
                    <ul>
                        <li>Morrillo de res</li>
                        <li>Zanahoria</li>
                        <li>Calabacína</li>
                        <li>Apio, ajo y cebolla</li>
                        <li>Mostaza</li>
                        <li>Pan integral</li>
                        <li>Lechuga, perejil y tomillo</li>
                        <li>Aceite de oliva</li>
                        <li>Espinaca</li>
                        <li>Aceite de oliva</li>
                        <li>Sal y pimienta</li>
                    </ul>
                    <hr>
                    <b>PREPARACIÓN</b> 
                    <p>
                    Primero picamos zanahoria, apio, cebolla y ajo y lo salteamos con aceite de oliva. Cuando esté bien dorado le ponemos agua y lo dejamos que hierva, 
                    una vez hirviendo le ponemos el morrillo. Previamente el morrillo debemos haberlo tostado por los lados, sellarlo en una sartén con apenas una gota 
                    de aceite de oliva, para que nos quede jugoso por dentro. Una vez sellado lo añadimos a la sartén con el agua abundante y lo dejamos cocer por ocho 
                    horas a fuego medio. Una vez que lo tengamos lo podemos retirar y dejar enfriar para proceder a preparar el plato.
                    Ponemos una mezcla de pan integral, lo tostamos, lo rallamos y le añadimos dos ajos crudos muy machacados, unas hojas de tomillo y un poquito de perejil 
                    y reservamos esa mezcla para empanizar luego el morrillo. 
                    <br><br>
                    Una vez tenemos el morrillo, lo untamos con un poco de mostaza, lo rebozamos en la mezcla del pan, y lo ponemos al horno para que quede tostado, para 
                    posteriormente proceder a cortarlo muy finito.
                    Rallamos por aparte zanahoria en crudo, la podemos hacer con un poquito de agua, sal y pimienta, luego le quitamos el exceso de agua y la ponemos a tostar. 
                    La remolacha por su parte la ponemos a cocer, la trituramos con aceite de oliva y hacemos un puré. Por último ponemos a asar una buena rodaja de calabacín.
                    <br><br>
                    Finalmente con todos nuestros ingredientes preparados y cocinados procedemos a montar el plato a semejanza de la imagen añadiendo un poco de lechuga y perejil, 
                    y ¡buen provecho!
