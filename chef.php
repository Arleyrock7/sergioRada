                <div>
                  <p>
                    <b>INGREDIENTES</b>
                    <ul>
                        <li>Filete de trucha sin espinas ni piel</li>
                        <li>Piel de la trucha</li>
                        <li>Zanahoria</li>
                        <li>Habichuela</li>
                        <li>Higos</li>
                        <li>Garbanzos</li>
                        <li>Hierbabuena</li>
                        <li>Cebolla morada, ajo, sal, perejil, pimienta y aceite de oliva</li>
                    </ul>
                    <hr>
                    <b>PREPARACIÓN</b> 
                    <p>
                    Lo primero, desde el día anterior, debemos poner en remojo los garbanzos en agua templada. Al día siguiente los ponemos en agua 
                    hirviendo hasta que nos queden en la textura blanda adecuada, mezclados con un poco del agua del garbanzo los trituramos. 
                    Posteriormente picamos cebolla morada con ajo, perejil y hierbabuena, todo eso se saltea con los garbanzos. Finalmente colocaremos 
                    en un molde para el acompañamiento a la hora de montar el plato.
                    A la trucha les quitamos la piel, la cual dejamos aparte para su posterior tratamiento, y las espinas. Una vez la tengamos 
                    lista, procederemos a enrollarla con la habichuela y la zanahoria dentro, previamente cortadas y hervidas y apartadas en 
                    agua fría para que no pierdan la intensidad de sus respectivos colores. 
                    <br><br>
                    Cuando la tengamos enrollada añadimos sal y pimienta, 
                    la ponemos en la sartén bien caliente por la parte donde se une la punta de la trucha con el lomo.
                    La piel de la trucha la colocamos en el horno durante 5 minutos a 180 grados con un poquito de aceite, la dejamos quieta, 
                    la sacamos y ya la tenemos lista y crujiente para montar nuestro plato.
                    <br><br>
                    Los higos los partimos al medio, los ponemos en el horno a 180 grados durante 15 minutos, les añadimos un poco de aceite 
                    de oliva y también están listos. Importante, los metemos al horno con la cáscara y una vez los tengamos se la quitamos, es muy fácil de quitar.
                    Finalmente montamos nuestro plato a semejanza de la imagen y ¡buen provecho!
