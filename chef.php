                    Una vez que tenemos el zucchini, le hacemos un huequito por dentro. Dentro colocaremos la hoja de col blanca. 
                    Ponemos dentro también la berenjena, cortamos en trozo la sobrebarriga y la disponemos también dentro del hueco 
                    de zucchini. Servimos junto a la salsa de tomate y listo.
