                    <hr>
                    <b>PREPARACIÓN</b> 
                    <p>
                    El calamar, una vez descongelado, le ponemos ajo y perejil y lo dejamos reposar de un día para otro.
                    El día del almuerzo primero rallamos la yuca y la ponemos en agua para que suelte el almidón, luego la sacamos, 
                    la escurrimos y la reservamos. Ese agua nos servirá para las alcachofas. Las alcachofas las limpiamos bien, les 
                    quitamos un poco de la superficie, un poco del tronco, las partimos en medio teniendo siempre al lado agua con 
                    perejil y un poquito de agua con vinagre o limón para evitar que se nos oxiden. Una vez partidas al medio y de 
                    quitarles la pelusilla del medio, las ponemos a cocer en el agua donde previamente pusimos la yuca a soltar almidón. 
                    <br><br>
                    Una vez que veamos que están bien blandas querrá decir que ya están cocidas, procedemos a escurrirlas, las sacamos, 
                    y ya las tenemos listas y reservadas para más adelante.
                    Las espinacas las cortamos en juliana, las salteamos primero y después las pasamos con el agua de cocción de las alcachofas, 
                    dejamos reducir, las sacamos y reservamos. 
                    <br><br>
                    El relleno del calamar: cortamos en juliana la zanahoria, la habichuela, la cebolla, salteamos junto con la yuca hasta que 
                    quede muy tostada. Reservamos el relleno. 
                    Una vez hecho el relleno, ayudados de una farsa de cocina, rellenamos el calamar y lo ponemos a asar con fuego bueno hasta 
                    que coja un color tostado.
                    Procedemos también a finalizar las alcachofas vuelta y vuelta por la sartén, donde también asamos los langostinos con un poco 
                    de sal y aceite con laurel, ajo y tomillo. 
                    <br>
                    Montamos los langostinos sobre las alcachofas ya sobre el plato final. Añadimos la lechuga y las espinacas, ponemos por 
                    último el calamar relleno y… ¡buen provecho!

