                <h3><span class="ui-accordion-header-icon ui-icon ui-accordion-icon ui-accordion-icon-active"></span>JAIBA RELLENA DE HABAS </h3>
                <div>
                  <p>
                    <b>INGREDIENTES</b>
                    <ul>
                        <li>Dos jaibas</li>
                        <li>Habas</li>
